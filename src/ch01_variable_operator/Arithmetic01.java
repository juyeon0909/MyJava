package ch01_variable_operator;

public class Arithmetic01 {
    static void main() {
        int x = 14, y = 5;

        //산술 연산자
        System.out.println("더하기 : " + (x + y));
        System.out.println("빼기 : " + (x - y));
        System.out.println("곱하기 : " + (x * y));
        System.out.println("나누기 : " + (x / y));
        System.out.println("나누기2 : " + (x % y));
    }
}
